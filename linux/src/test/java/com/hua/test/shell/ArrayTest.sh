# 二维数组 (模拟实现)
for ((i=0;i<2;i++))
do (
	for ((j=0;j<3;j++))
	do 
	(
		# 输入
		read man$i$j
		# man$i$j =  $i + $j
	)
	done
)
done

# 循环输出
for ((i=0;i<2;i++))
do
(
	for ((j=0;j<3;j++))
	do
	(
		eval echo -n "\$man$i$j"
	)
	done
)
printf "\n"
done

